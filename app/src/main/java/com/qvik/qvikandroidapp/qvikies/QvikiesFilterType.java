package com.qvik.qvikandroidapp.qvikies;

/**
 * Used with the filter spinner in the qvikies list.
 */
public enum QvikiesFilterType {
    /**
     * Does not filter qvikies.
     */
    ALL_QVIKIES,

    /**
     * Filters only the engineers.
     */
    ENGINEERS,

    /**
     * Filters only the designers.
     */
    DESIGNERS
}
